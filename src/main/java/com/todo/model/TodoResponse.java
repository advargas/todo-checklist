package com.todo.model;

public class TodoResponse {
	
	private boolean result;
	
	public TodoResponse() {
	}

	public TodoResponse(boolean result) {
		super();
		this.result = result;
	}

	public boolean isResult() {
		return result;
	}

	public void setResult(boolean result) {
		this.result = result;
	}
	
	

}
